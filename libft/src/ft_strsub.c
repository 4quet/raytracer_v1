#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	unsigned int	i;
	char			*str;

	i = 0;
	if (s)
	{
		str = (char *)malloc(sizeof(*str) * len + 1);
		if (!str)
			return (NULL);
		while (i < len && s[i] != '\0')
		{
			str[i] = s[start + i];
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
