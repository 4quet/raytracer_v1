#include "libft.h"

int	ft_imaxlen(intmax_t n)
{
	int	i;

	i = 0;
	if (n < -9223372036854775807)
		return (20);
	if (n == 0)
		return (1);
	if (n < 0)
	{
		i++;
		n = -n;
	}
	while (n > 0)
	{
		i++;
		n = n / 10;
	}
	return (i);
}
