#include "libft.h"

int	ft_nbrlen(int nbr)
{
	int	i;

	i = 0;
	if (nbr < 0)
	{
		i++;
		nbr = -nbr;
	}
	if (nbr == 0)
		return (1);
	while (nbr > 0)
	{
		i++;
		nbr = nbr / 10;
	}
	return (i);
}
